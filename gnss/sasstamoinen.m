function [dTD, dTW] = sasstamoinen(phi, H, P, T, Hr)

	c = 299792458;

	H = H / 1000;
	T = T + 273.15;

	e0 = 6.108 .* Hr ./ 100 .* exp((17.15 .* T - 4684) ./ (T - 38.5));

	dTD = 1 ./ c .* 0.0022777 .* (1 + 0.0026 .* cosd(2 .* phi) + 0.00028 .* H) .* P;
	dTW = 1 ./ c .* 0.0022777 .* (1255 ./ T + 0.05) .* e0;
end