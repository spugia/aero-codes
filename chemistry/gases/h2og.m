function [Cp, k, R, Mw] = h2og(T)

	Ru = 8.314462618;
	Mw = 18.01528E-3;
	R  = Ru / Mw;

	%.. specific heat ratio
	%.. NIST
	A = [30.09200, 41.96426];
	B = [6.832514, 8.622053];
	C = [6.793435, -1.499780];
	D = [-2.534480, 0.098119];
	E = [0.082139, -11.15764];
	F = [-250.8810, -272.1797];
	G = [223.3967, 219.7809];

	if T < 1700

		n = 1;

	elseif T < 6000

		n = 2;
	end

	T = T / 1000;

	Cp = A(n)+B(n)*T+C(n)*T^2+D(n)*T^3+E(n)/T^2;

	Cp = Cp / Mw;

	Cv = Cp - R;
	k  = Cp / Cv;
end