function [Cp, k, R, Mw] = co2g(T)

	Ru = 8.314462618;
	Mw = 44.0095E-3;
	R  = Ru / Mw;

	%.. specific heat ratio
	%.. NIST
	A = [24.99735, 58.16639];
	B = [55.18696, 2.720074];
	C = [-33.69137, -0.492289];
	D = [7.948387, 0.038844];
	E = [-0.136638, -6.447293];
	F = [-403.6075, -425.9186];
	G = [228.2431, 263.6125];

	if T < 1200

		n = 1;

	elseif T < 6000

		n = 2;
	end

	T = T / 1000;

	Cp = A(n)+B(n)*T+C(n)*T^2+D(n)*T^3+E(n)/T^2;

	Cp = Cp / Mw;

	Cv = Cp - R;
	k  = Cp / Cv;
end