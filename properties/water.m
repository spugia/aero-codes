%.. computes thermodynamic properties of water

% T = temperature of water

function [Hv, s, Pv, Cp, Cv, k, R, Mw] = water(T)

	Mw = 18.01528E-3;
	R = 461.52;

	%.. vapor pressure [Pa]
	%.. NIST chemical database
	A = 5.40221;
	B = 1838.675;
	C = -31.737;

	Pv = 10.^(A-B./(T+C) + 5);

	%.. surface tension
	%https://srd.nist.gov/JPCRD/jpcrd231.pdf
	B = 235.8E-3;
	b = -0.625;
	mu = 1.256;
	Tc = 647.15;

	s = B.*((Tc - T) ./ Tc).^mu.*(1+b.*((Tc - T) ./ Tc));

	%.. heat of vaporization
	%.. engineering toolbox
	Hvs = [2500.9 2496.2 2491.4 2477.2 2467.7 2458.3 2453.5 2441.7 2429.8 2420.3 2406 2396.4 2381.9 2372.3 2357.7 2333 2308 2282.5 2266.9 2256.4 2229.6 2202.1 2144.3 2082 2014.2 1939.7 1857.4 1765.4 1661.6 1543 1404.6 1238.4 1027.3 719.8];
	Ts = [0.01 2 4 10 14 18 20 25 30 34 40 44 50 54 60 70 80 90 96 100 110 120 140 160 180 200 220 240 260 280 300 320 340 360];

	Hv = interp1(Ts, Hvs, T, 'linear') .* 1000;

	%.. specific heat ratio
	%.. engineering tool box
	Ts = [175 200 225 250 275 300 325 350 375 400 450 500 550 600 650 700 750 800 850 900 950 1000 1050 1100 1150 1200 1250 1300 1350 1400 1500 1600 1700 1800 1900 2000 2100 2200 2300 2400 2500 2600 2700 2800 2900 3000 3500 4000 4500 5000 5500 6000];
	Cps = [1.85 1.851 1.852 1.855 1.859 1.864 1.871 1.88 1.89 1.901 1.926 1.954 1.984 2.015 2.047 2.08 2.113 2.147 2.182 2.217 2.252 2.288 2.323 2.358 2.392 2.425 2.458 2.49 2.521 2.552 2.609 2.662 2.711 2.756 2.798 2.836 2.872 2.904 2.934 2.962 2.987 3.011 3.033 3.053 3.072 3.09 3.163 3.217 3.258 3.292 3.322 3.35];

	Cp = interp1(Ts, Cps, T) .* 1000;

	k = Cp / (Cp - R);
end